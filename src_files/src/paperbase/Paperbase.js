function Paperbase(props) {
  const { classes } = props;
  const [mobileOpen, setMobileOpen] = React.useState(false);

  const [mainUI, setMainUI] = React.useState('In the Zone')

  const handleDrawerToggle = () => {
    setMobileOpen(!mobileOpen);
  };

  var onClickHandler = (e, childId) =>{
    e.preventDefault();
    if(childId !== undefined){
      setMainUI(childId)
    }
  }
  var mainPage;
  if(mainUI === "In the Zone"){
    mainPage = (<Container fluid>
                  <Row>
                    <Col>
                      <Timer history={props.history}/>
                    </Col>
                    <Col xs={8}>
                      <Game history={props.history}/>
                    </Col>
                  </Row>
                </Container>)
  }
  else if(mainUI === "Policies"){
    mainPage = <Policy history={props.history}/>
  }
  else{
    mainPage = <About history={props.history}/>
  }


  return (
    <ThemeProvider theme={theme}>
      <div className={classes.root}>
        <CssBaseline />
        <nav className={classes.drawer}>
          <Hidden smUp implementation="js">
            <Navigator
              PaperProps={{ style: { width: drawerWidth } }}
              variant="temporary"
              open={mobileOpen}
              onClose={handleDrawerToggle}
            />
          </Hidden>
          <Hidden xsDown implementation="css">
            <Navigator PaperProps={{ style: { width: drawerWidth } }} onClick={onClickHandler} />
          </Hidden>
        </nav>
        <div className={classes.app}>
          <Header onDrawerToggle={handleDrawerToggle} header={mainUI} logout={props.logout}/>
          <main className={classes.main}>
            {mainPage}
          </main>
          <footer className={classes.footer}>
            <Copyright />
          </footer>
        </div>
      </div>
    </ThemeProvider>
  );
}

Paperbase.propTypes = {
  classes: PropTypes.object.isRequired,
};

export default withStyles(styles)(Paperbase);
